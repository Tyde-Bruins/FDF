int	ft_atoi(const char *s)
{
	int i;
	int res;
	int is_neg;

	res = 0;
	is_neg = 0;
	i = 0;
	while ((s[i] == '\n') || (s[i] == '\t') || (s[i] == '\v') ||
			(s[i] == ' ') || (s[i] == '\f') || (s[i] == '\r'))
		i++;
	if (s[i] == '-')
		is_neg = 1;
	if (s[i] == '+' || s[i] == '-')
		i++;
	while (s[i] && (s[i] >= '0') && (s[i] <= '9'))
	{
		res = (10 * res) + s[i] - '0';
		i++;
	}
	if (is_neg == 1)
		return (-res);
	else
		return (res);
}
