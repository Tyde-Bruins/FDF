char	*ft_strrchr(const char *s, int c)
{
	char *res;

	res = 0;
	while (*s++)
		if (*(s - 1) == c)
			res = (char*)(s - 1);
	if (!c)
		return ((char*)--s);
	else
		return (res);
}
