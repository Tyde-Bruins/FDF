int	ft_getwcntsd(char const *s, char c)
{
	int res;

	res = 0;
	while (*s)
	{
		if (*s != c)
		{
			while (*s != c && *s)
				s++;
			res++;
		}
		else
			s++;
	}
	if (res == 0)
		return (1);
	return (res);
}
