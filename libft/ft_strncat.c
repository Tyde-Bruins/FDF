#include <stdlib.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *res;

	res = s1;
	while (*s1)
		s1++;
	while (n--)
		if (!(*s1++ = *s2++))
			return (res);
	*s1 = 0;
	return (res);
}
